public class BSupervivientes {
    private String idHumanos;
    private Float peso;
    private Float fuerza;
    private String idPareja;

    public String getIdHumanos() {
        return idHumanos;
    }

    public void setIdHumanos(String idHumanos) {
        this.idHumanos = idHumanos;
    }

    public Float getPeso() {
        return peso;
    }

    public void setPeso(Float peso) {
        this.peso = peso;
    }

    public Float getFuerza() {
        return fuerza;
    }

    public void setFuerza(Float fuerza) {
        this.fuerza = fuerza;
    }

    public String getIdPareja() {
        return idPareja;
    }

    public void setIdPareja(String idPareja) {
        this.idPareja = idPareja;
    }
}
